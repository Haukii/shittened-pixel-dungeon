package com.shatteredpixel.shatteredpixeldungeon.items.misc;

import com.shatteredpixel.shatteredpixeldungeon.items.Item;
import com.shatteredpixel.shatteredpixeldungeon.items.Recipe;
import com.shatteredpixel.shatteredpixeldungeon.items.quest.DarkGold;
import com.shatteredpixel.shatteredpixeldungeon.sprites.ItemSpriteSheet;

public class GoldBar extends Item {
	
	{
		image = ItemSpriteSheet.GOLDBAR;
		
		stackable = true;
	}
	
	@Override
	public boolean isUpgradable() {
		return false;
	}
	
	@Override
	public boolean isIdentified() {
		return true;
	}

	public static class Craft extends Recipe.SimpleRecipe{
		{
			inputs =  new Class[]{DarkGold.class};
			inQuantity = new int[]{3};

			cost = 2;

			output = GoldBar.class;
			outQuantity = 1;
		}
	}
}
