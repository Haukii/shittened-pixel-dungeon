package com.shatteredpixel.shatteredpixeldungeon.items.stones;

import com.shatteredpixel.shatteredpixeldungeon.Assets;
import com.shatteredpixel.shatteredpixeldungeon.effects.Speck;
import com.shatteredpixel.shatteredpixeldungeon.items.Item;
import com.shatteredpixel.shatteredpixeldungeon.items.misc.Painting;
import com.shatteredpixel.shatteredpixeldungeon.items.weapon.Weapon;
import com.shatteredpixel.shatteredpixeldungeon.items.weapon.melee.MeleeWeapon;
import com.shatteredpixel.shatteredpixeldungeon.mechanics.Prefix;
import com.shatteredpixel.shatteredpixeldungeon.messages.Messages;
import com.shatteredpixel.shatteredpixeldungeon.sprites.ItemSpriteSheet;
import com.shatteredpixel.shatteredpixeldungeon.utils.GLog;
import com.watabou.noosa.audio.Sample;
import com.watabou.utils.Random;

public class StoneOfReforging extends InventoryStone {
	
	{
		image = ItemSpriteSheet.STONE_REFORGE;
	}

	@Override
	protected boolean usableOnItem(Item item) {
		return item instanceof MeleeWeapon;
	}
	
	@Override
	protected void onItemSelected(Item item) {

		((Weapon)item).reforge(Prefix.random());
		
		curUser.sprite.emitter().start( Speck.factory( Speck.LIGHT ), 0.1f, 5 );
		Sample.INSTANCE.play(Assets.Sounds.UPGRAGE_ARMOR,1, Random.Float(0.9f,1.1f));

		GLog.p(Messages.get(this, "weapon"));
		
		useAnimation();
		updateQuickslot();
	}
}
